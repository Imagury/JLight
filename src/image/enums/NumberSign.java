package image.enums;

/**
 * A very simple enum specifying whether a number is positive or negative.
 * Used in number-generating methods.
 * @param POSITIVE (+)
 * @param NEGATIVE (-)
 * @param UNDEFINED (+/-)
 * @author Image (cursustemporum)
 */
public enum NumberSign {
	POSITIVE,
	NEGATIVE,
	UNDEFINED;
}
