package imagury.enums;

/**
 * A very simple enum specifying whether a generated number should be positive or negative.
 * Used in number-generating methods.
 * @param POSITIVE (+)
 * @param NEGATIVE (-)
 * @param UNDEFINED (+/-)
 * @since JLight 0.1.0
 * @author Imagury 
 */
public enum NumberSign {
	POSITIVE,
	NEGATIVE,
	UNDEFINED;
}
